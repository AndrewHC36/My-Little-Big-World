# a-air; b-stone


def generator(seed, size):
    s = seed
    ter = []
    for i in range(size[0]):  # Width
        s = []
        rand = r.randint(0, 4)
        for j in range(size[1]):  # Hieght
            if 25 < j < 30:
                if j > 25+rand: s.append("b")
                else: s.append("a")
            elif j < 26: s.append("a")
            else: s.append("c")
        ter.append("".join(s))
    return ter

